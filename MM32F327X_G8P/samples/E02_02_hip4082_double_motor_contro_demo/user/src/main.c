// *************************** 例程测试说明 ***************************
// 1.核心板烧录完成本例程 主板电池供电
// 
// 2.如果接了电机 可以看到电机周期正反转
// 
// 3.如果没有接电机 使用万用表可以在驱动电机输出端子上测量到输出电压变化
